    void cio_clear_screen(void)
    {
        display.clearDisplayArea(0, 16, 128, 48);
    }

    void cio_draw_background(void)
    {
        cio_clear_screen();
        display.drawBitmap(0, 16, old_clock_face_background, 128, 48, 1);
    }

    void cio_draw_hour_hand(int hour, int minute)
    {

        int hour_angle = 0.5 * (60 * (hour % 12) + minute);

        float hour_a = tan((90 - hour_angle) * PI / 180.0);

        if (hour_angle > 0 && hour_angle < 180)
        {
            for (int h = 0; h < hour_hand_pixels; h++)
            {
                display.writePixel(h + 63, -hour_a * h + 39, 1);
            }
        }

        if (hour_angle > 180 && hour_angle < 360)
        {
            for (int h = 0; h < hour_hand_pixels; h++)
            {
                display.writePixel(-h + 63, hour_a * h + 39, 1);
            }
        }
        if (hour_angle == 0 || hour_angle == 180)
        {
            if (hour_angle == 0)
            {
                for (int h = 0; h < hour_hand_pixels; h++)
                {
                    display.writePixel(63, -h + 39, 1);
                }
            }
            else
            {
                for (int h = 0; h < hour_hand_pixels; h++)
                {
                    display.writePixel(63, h + 39, 1);
                }
            }
        }
        if (hour_angle == 90 || hour_angle == 270)
        {
            if (hour_angle == 0)
            {
                for (int h = 0; h < hour_hand_pixels; h++)
                {
                    display.writePixel(h + 63, 39, 1);
                }
            }
            else
            {
                for (int h = 0; h < hour_hand_pixels; h++)
                {
                    display.writePixel(-h + 63, 39, 1);
                }
            }
        }
    }
    void cio_draw_minute_hand(int minute)
    {

        int minute_angle = 6 * minute;
        float minute_a = tan((90 - minute_angle) * PI / 180.0);

        if (minute_angle > 0 && minute_angle < 180)
        {
            for (int h = 0; h < minute_hand_pixels; h++)
            {
                display.writePixel(h + 63, -minute_a * h + 39, 1);
            }
        }

        if (minute_angle > 180 && minute_angle < 360)
        {
            for (int h = 0; h < minute_hand_pixels; h++)
            {
                display.writePixel(-h + 63, minute_a * h + 39, 1);
            }
        }
        if (minute_angle == 0 || minute_angle == 180)
        {
            if (minute_angle == 0)
            {
                for (int h = 0; h < 6; h++)
                {
                    display.writePixel(63, -h + 39, 1);
                }
            }
            else
            {
                for (int h = 0; h < 6; h++)
                {
                    display.writePixel(63, h + 39, 1);
                }
            }
        }
        if (minute_angle == 90 || minute_angle == 270)
        {
            if (minute_angle == 0)
            {
                for (int h = 0; h < minute_hand_pixels; h++)
                {
                    display.writePixel(h + 63, 39, 1);
                }
            }
            else
            {
                for (int h = 0; h < minute_hand_pixels; h++)
                {
                    display.writePixel(-h + 63, 39, 1);
                }
            }
        }
    }

    void cio_draw_hands(int hour, int minute)
    {

        cio_draw_hour_hand(hour, minute);
        cio_draw_minute_hand(minute);

        display.display();
    }
    void cio_remove_old_hands(int hour, int minute) {}
    void cio_update_hands(int old_hour, int old_minute, int hour, int minute) {}

public:
    ClockfaceAnalogController() {}

    void drawAnalogFace(int hour, int minute)
    {
        cio_draw_background();
        cio_draw_hands(hour, minute);
    }
